#include <stdio.h>

//声明变量的格式:类型说明符 + 空格+变量名列表 

int main()
{
    /********整型变量*********/
    //基本整型变
    int a;//声明一个int类型的变量 
    unsigned b;
    //短整型 
    short c;
    unsigned short d;
    //长整型 
    long e;
    unsigned long f;
    
    /********浮点型变量**********/
    //单精度 
    float h;
    //双精度
    double i; 
    
    //字符型 
    char j;
    unsigned char k;

    return 0;    
}
